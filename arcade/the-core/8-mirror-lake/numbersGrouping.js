const assert = require('assert')

function numbersGrouping (a) {
  let group
  return Object.keys(a.reduce((acc, val) => {
    group = Math.ceil(val / 10000)
    if (!acc[group]) acc[group] = true
    return acc
  }, {})).length + a.length
}

const test = (a, expected) => assert.strictEqual(numbersGrouping(a), expected)

test([20000, 239, 10001, 999999, 10000, 20566, 29999], 11)
